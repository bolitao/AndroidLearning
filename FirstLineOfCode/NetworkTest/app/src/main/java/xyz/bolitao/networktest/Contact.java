package xyz.bolitao.networktest;

public class Contact {
    private String userId;
    private String friendId;
    private String becomeFriendDate;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getFriendId() {
        return friendId;
    }

    public void setFriendId(String friendId) {
        this.friendId = friendId;
    }

    public String getBecomeFriendDate() {
        return becomeFriendDate;
    }

    public void setBecomeFriendDate(String becomeFriendDate) {
        this.becomeFriendDate = becomeFriendDate;
    }
}
